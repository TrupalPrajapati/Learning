// number datatype
let a = 10;
console.log(typeof(a));
console.log(a);

//string datatype
let b = "This is string";
console.log(typeof(b),b);

//boolean datatype
let c = true;
console.log(typeof(c),c);

//null datatype
let d = null;
console.log(typeof(d),d);

//undefined datatype
let e;
console.log(typeof(e),e);